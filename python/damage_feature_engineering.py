import re

def find_length(line):

    line = normalize_numeric(line.lower())

    # =====================================================
    # PRIORITAS 1
    # panjang 120 meter
    # panjang 5 m
    # panjang ± 20 meter
    # =====================================================

    patterns = [

        r'panjang\s*[:=]?\s*±?\s*(\d+(?:\.\d+)?)\s*(km|meter|m)',

        r'sepanjang\s*±?\s*(\d+(?:\.\d+)?)\s*(km|meter|m)',

        r'(\d+(?:\.\d+)?)\s*(km|meter|m)\s*panjang'

    ]

    for pattern in patterns:

        m = re.search(pattern, line)

        if m:

            value = float(m.group(1))

            unit = m.group(2)

            if unit == "km":

                value *= 1000

            return value

    # =====================================================
    # fallback
    # =====================================================

    return 0

#Parser KK
def find_kk(line):

    line = line.lower()

    m = re.search(

        r"(\d+)\s*kk",

        line

    )

    if m:

        return int(m.group(1))

    return 0

