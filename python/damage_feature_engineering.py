import html
import re
import pandas as pd

def normalize_html(text):

    if pd.isna(text):
        return ""

    text = str(text)

    text = html.unescape(text)

    # <br> -> newline
    text = re.sub(
        r"<br\s*/?>",
        "\n",
        text,
        flags=re.IGNORECASE
    )

    # </div> -> newline
    text = re.sub(
        r"</div>",
        "\n",
        text,
        flags=re.IGNORECASE
    )

    # hapus tag lain
    text = re.sub(
        r"<[^>]+>",
        " ",
        text
    )

    return text

