def split_damage_text(text):

    text = clean_damage(text)

    lines = []

    for line in text.split("\n"):

        line = line.strip()

        if line == "":
            continue

        # hapus bullet di awal baris saja
        line = re.sub(
            r"^[-•*]+\s*",
            "",
            line
        )

        lines.append(line)

    return lines

