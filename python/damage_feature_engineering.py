def find_entity(line):

    line = line.lower()

    entity_found = None

    best_position = 999999

    for entity, keywords in ENTITY_DICT.items():

        for keyword in keywords:

            pos = line.find(keyword)

            if pos != -1:

                if pos < best_position:

                    best_position = pos

                    entity_found = entity

    return entity_found

