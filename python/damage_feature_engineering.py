def normalize_symbol(text):

    symbol_map = {

        "•":"\n",

        "➸":"\n",

        "*":"\n",

        "|":"\n",

        ";":"\n"

    }

    for old,new in symbol_map.items():

        text = text.replace(
            old,
            new
        )

    return text

